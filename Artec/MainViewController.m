    // Do any additional setup after loading the view, typically from a nib.
    self.title = @"Shops";
    
    [self.tableView registerNib:[UINib nibWithNibName:@"LocationTableViewCell" bundle:nil] forCellReuseIdentifier:cellIdentifier];
    [self requestData];
}


- (void)didReceiveMemoryWarning {
    [super didReceiveMemoryWarning];
    // Dispose of any resources that can be recreated.
}

- (void)requestData {
    [[RKObjectManager sharedManager] getObjectsAtPath:@"/personalcabinet/api/v1/geo/poi?lnglat=30.4292085407624%2C59.899987687083&radius=5736"
